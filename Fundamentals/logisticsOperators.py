#Operadores AND 
#Las dos condiciones tienen que ser verdaderas si o si para devolver true
operator_and = True & True #True
operator_and1 = False & True #False 
operator_and2 = True & False #False
operator_and3 = False & False #False 

#Operadores OR
#Al menos una codicion tiene que ser verdadera para devolver true
operator_or = True | True #True 
operator_or1 = False | True #True 
operator_or2 = True | False #True 
operator_or3 = False | False #False

#Operadores NOT
#Convierte el valor a un valor contrario    
operator_not = not True # Falso
operator_not1 = not False # True
